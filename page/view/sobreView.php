                    lo em produto comercializável. Então, resolveu-se inscrever o projeto em um
                    edital para a busca de recursos no Conselho Nacional de Desenvolvimento
                    Científico e Tecnológico (CNPq) para o desenvolvimento de novos produtos. O
                    projeto foi contemplado, o que proporcionou o início de desenvolvimento do
                    produto e o começo de novos caminhos e transformações. A ideia apresentava
                    grande potencial, resultando no surgimento da empresa BaconWare, com o
